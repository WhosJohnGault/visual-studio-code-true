#store a name in a string
name="john doe the third"
# name.title() takes the information in the string and then capitalizes it whenever there is a space
#title is considered a method, a method is a action that python can perform on a piece of data
print(name.title())
#we can print strings as being all upppercase or all lowercase letters using the upper and lower methods
print(name.upper())
print(name.lower())
