#Lists are identified by the compiler by using square brackets([]), elements are comma
#seperated
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles)
#the above statement prints out all elements of the list in a single line. 
#we can print individual elements by denoting the string and picking an element
#by using square brackets, the first element is denoted with [0]
#as an example refer to the below print function.
print(bicycles[0])
#we can use the title() method to better format our output.
print(bicycles[0].title())
