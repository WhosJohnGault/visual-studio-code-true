friends=['Gabby','Matthew','Jessie','Zack','Fernando','Jeselle','Jonah Hill']
print(friends[0:5]) #[0:5] is a slice to include the first five entries in the list
print(friends[:5]) #excluding zero will just include the first five entries
print(friends[2:5]) #two will start the print function at the third entry
print(friends[3:]) #this will print from the fouth entry to the end of the list